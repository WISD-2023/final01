    public function showOrderPage()
    {
        $cartItems = Cart::where('user_id', auth()->id())->get();

        $totalPrice = $cartItems->sum(function ($item) {
            return $item->quantity * $item->product->price;
        });

        return view('order.order', compact('cartItems', 'totalPrice'));
